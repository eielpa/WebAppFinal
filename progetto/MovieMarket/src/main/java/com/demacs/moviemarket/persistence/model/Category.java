package com.demacs.moviemarket.persistence.model;

public class Category {

    private int id;
    private String name;

    // Getter e setter per id
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    // Getter e setter per name
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
